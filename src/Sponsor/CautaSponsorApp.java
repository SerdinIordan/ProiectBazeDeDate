    private void CautaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CautaButtonActionPerformed
        // TODO add your handling code here:
        try {
					String lastName = NumeSponsorTextField.getText();

					List<Sponsor> employees = null;

					if (lastName != null && lastName.trim().length() > 0) {
						employees = employeeDAO.searchSponsor(lastName);
					} else {
						employees = employeeDAO.getAllSponsor();
					}
					
					// create the model and update the "table"
					SponsorTableModel model = new SponsorTableModel(employees);
					
					table.setModel(model);
					
					/*
					for (Employee temp : employees) {
						System.out.println(temp);
					}
					*/
				} catch (Exception exc) {
					JOptionPane.showMessageDialog(CautaSponsorApp.this, "Error: " + exc, "Error", JOptionPane.ERROR_MESSAGE); 
				}
        
        
    }//GEN-LAST:event_CautaButtonActionPerformed

