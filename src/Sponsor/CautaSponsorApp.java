	public void refreshSponsorView() {

		try {
			List<Sponsor> employees = employeeDAO.getAllSponsor();

			// create the model and update the "table"
			SponsorTableModel model = new SponsorTableModel(employees);

			table.setModel(model);
		} catch (Exception exc) {
			JOptionPane.showMessageDialog(this, "Error: " + exc, "Error",
					JOptionPane.ERROR_MESSAGE);
		}
		
	}
    
    
    
