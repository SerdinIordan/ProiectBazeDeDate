    private void UpdateSponsorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateSponsorButtonActionPerformed
        // TODO add your handling code here:
        // get the selected item
				int row = table.getSelectedRow();
				
				// make sure a row is selected
				if (row < 0) {
					JOptionPane.showMessageDialog(CautaSponsorApp.this, "Trebuie sa selectati un sponsor", "Error",
							JOptionPane.ERROR_MESSAGE);				
					return;
				}
				
				// get the current employee
				Sponsor tempSponsor = (Sponsor) table.getValueAt(row, SponsorTableModel.OBJECT_COL);
				
				// create dialog
				SponsorDialog dialog = new SponsorDialog(CautaSponsorApp.this, employeeDAO, 
															tempSponsor, true);

				// show dialog
				dialog.setVisible(true);
        
    }//GEN-LAST:event_UpdateSponsorButtonActionPerformed

    private void DeleteSponsorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteSponsorButtonActionPerformed
        // TODO add your handling code here:
        
				try {
					// get the selected row
					int row = table.getSelectedRow();

					// make sure a row is selected
					if (row < 0) {
						JOptionPane.showMessageDialog(CautaSponsorApp.this, 
								"Trebuie sa selectati un Sponsor", "Error", JOptionPane.ERROR_MESSAGE);				
						return;
					}

					// prompt the user
					int response = JOptionPane.showConfirmDialog(
							CautaSponsorApp.this, "Delete this sponsor?", "Confirm", 
							JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

					if (response != JOptionPane.YES_OPTION) {
						return;
					}

					// get the current employee
					Sponsor tempEmployee = (Sponsor) table.getValueAt(row, SponsorTableModel.OBJECT_COL);

					// delete the employee
                                        employeeDAO.deleteSponsor(tempEmployee.getSponsorID());
					//employeeDAO.deleteEmployee(tempEmployee.getId());

					// refresh GUI
					refreshSponsorView();

					// show success message
					JOptionPane.showMessageDialog(CautaSponsorApp.this,
							"Sponsor deleted succesfully.", "Sponsor Deleted",
							JOptionPane.INFORMATION_MESSAGE);

				} catch (Exception exc) {
					JOptionPane.showMessageDialog(CautaSponsorApp.this,
							"Error deleting sponsor: " + exc.getMessage(), "Error",
							JOptionPane.ERROR_MESSAGE);
				}
        
    }//GEN-LAST:event_DeleteSponsorButtonActionPerformed

